#          0   1   2   3   4   5  6   7
numbers = [23, 56, 90, 12, 11, 4, 5, 78]

total = 0

# Loop through each number in the list
for number in numbers:
    # Add the current number to the total
    total += number

print(f"The total of the list is {total}")
print("The total of the list is " + str(total))
