# Artithemetic operators

print("Addition:", 2 + 2)
print("Subtraction:", 4 - 2)
print("Multiplication:", 4 * 2)
print("Division:", 5 / 2)

print("Modulo:", 4 % 2)
# 5 / 2 = 2 R 1
# 4 / 2 = 2 R 0
# 13 / 7 = 1 R 6

print("Floor Division:", 5 // 2)  # round down
print("Exponentiation:", 2 ** 4)  # 2 * 2 * 2 * 2 = 16
